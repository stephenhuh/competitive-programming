def uniquePathsWithObstacles(obstacleGrid):
    """
    :type obstacleGrid: List[List[int]]
    :rtype: int
    """
    num_rows = len(obstacleGrid)
    num_cols = len(obstacleGrid[0])
    
    if num_rows is 1:
        if any(col is 1 for col in obstacleGrid[0]):
            return 0
        else:
            return 1
    
    if num_cols is 1:
        for row in range(num_rows):
            if obstacleGrid[row][0] is 1:
                return 0
        return 1
                
    return backtrack(obstacleGrid, 0, 0, num_rows, num_cols)
    

def backtrack(grid, r, c, num_rows, num_cols):
    if r is 0 and c is 0 and (grid[r][c] is not 1):
        return backtrack(grid, r+1, c, num_rows, num_cols) + backtrack(grid, r, c+1, num_rows, num_cols)
    
    if r >= num_rows or c >= num_cols:
        return 0
    
    if grid[r][c] is 1:
        return 0
    
    if r is num_rows - 1 and c is num_cols - 1:
        return 1
    
    return backtrack(grid, r+1, c, num_rows, num_cols) + backtrack(grid, r, c+1, num_rows, num_cols)

